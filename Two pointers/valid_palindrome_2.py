# Given a string s, determine if it can be made into a palindrome by removing at most one character.


class Solution:
    def validPalindrome(self, s: str) -> bool:
        def palindrome(l, r):
            while l < r:
                if s[l] != s[r]:
                    return False
                l += 1
                r -= 1
            return True

        left, right = 0, len(s) - 1
        while left < right:
            if s[left] != s[right]:
                return palindrome(left + 1, right) or palindrome(left, right - 1)
            left += 1
            right -= 1
        return True


# Example 1:
# Input:
# {"s":"aba"}
# Output:
# true

# Example 2:
# Input:
# {"s":"abca"}
# Output:
# true
